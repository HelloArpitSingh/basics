public class Basics {
    public static void main(String[] args) {

        //Hello World
        System.out.println("Hello World");

        //Data Types
        //Primitive Data Types(sorted by bit size)

        //boolean-> stores only true or false
        boolean bool = true;

        //byte-> stores 1byte of data           || 1byte = 8bits
        byte b = 10;

        //char-> stores only a single character || char = 2bytes or 16bits
        char ch = 'A';

        //integer types
        //int-> stores only integer values, no fractions                 || int = 4bytes or 32bits
        int num = 10;
        //long-> stores integer values but larger than int, no fractions || long = 8bytes or 64bits
        long longNum = 1000200;

        //floating types -> not precise, should not be used for scientific or financial purposes
        //float-> stores fractional values, but is not precise  || float 4bytes or 32bits
        float f = 3.14f;
        //double-> stores fractional values, but is not precise || float 8bytes or 64bits
        double d = 3.1415;

    }
}
